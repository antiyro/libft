int	ft_strncmp(const char *s1, const char *s2, int n)
{
	if (n == 0)
		return (0);
	while (n)
	{
		if (*s1 != *s2)
			return (*s1 - *s2);
		if (!*s1)
			break ;
		s1++;
		s2++;
		n--;
	}
	return (0);
}
