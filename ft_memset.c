void	*ft_memset(void *s, int c, int n)
{
	int i;
	unsigned char *my_s;

	i = 0;
	my_s = s;
	while (my_s[i] && n > 0)
	{
		my_s[i] = (unsigned char)c;
		n--;
		i++;
	}
	return (s);
}